
public class AfficherIEEE {

	public static void main(String[] args) {
		float x = 15;
		float z = 0;
		float a = x/z;
		float y = -123;
		float b = y/z;
		float c = a/b;
		
		System.out.println("x = " + x);
		System.out.println("z = " + z);
		System.out.println("a = " + a);
		System.out.println("y = " + y);
		System.out.println("b = " + b);
		System.out.println("c = " + c);

	}

}
